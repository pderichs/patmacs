use strict;
use warnings;

my $datetime = localtime();
my $out_path = $ARGV[0];

open(my $fh, '>', $out_path) or die "Unable to open file: $!";

print($fh ";; -*- lexical-binding: t; -*-\n");
print($fh ";; generated by patmacs script ($datetime)\n");
print($fh "(require 'cl-lib)\n");
print($fh "(setq path-dirs '(");

my @paths = split(/:/, $ENV{'PATH'});
for my $path (@paths) {
    (my $escaped = $path) =~ s/([\\"])/\\$1/g;
    print($fh qq{  "$escaped"\n});
}

print($fh "))\n");
print($fh "(setq exec-path\n");
print($fh "  (cl-remove-duplicates (append path-dirs exec-path)\n");
print($fh "                         :test #'string= :from-end t))\n");
print($fh "(setenv \"PATH\" (mapconcat #'identity exec-path path-separator))\n");
close($fh);

print("Done.\n");
